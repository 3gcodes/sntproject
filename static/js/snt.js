$(function () {
  $('.btn-primary').on('click', function (e) {
    e.preventDefault();
    $('.loading').hide();
    $('.content').hide();
    $('.panel').hide();
    var articleAddress = $('#articleAddress').val();
    if (isUrlValid(articleAddress)) {
      fetch(articleAddress)
    } else {
      $('.panel').show();
      $('.panel-body').text('Invalid URL');
    }
  });
});

function fetch(address) {
  $('.panel').hide();
  $('.loading').show();
  $('.btn-primary').prop('disabled', 'disabled');
  $.getJSON('/snt/fetch.php', {address: address}, function (data) {
    $('.loading').hide();
    $('.content').show();
    if (data.article.success) {
      $('#photo').attr('src', data.article.photo);
      $('#headline').text(data.article.headline);
      $('#story').text(data.article.story);
    } else {
      $('.panel').show();
      $('.panel-body').text('Error retrieving story');
    }

    $('.btn-primary').removeAttr('disabled');
  });
}

